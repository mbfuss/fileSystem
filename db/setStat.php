<?php
// Настройки подключения
$servername = "localhost";
$username = "root";
$password = "12346";
$dbname = "fsDataBase";

// Получение данных из запроса
$путь = $_POST['путь'];     // Путь к файлу (ожидается из POST-запроса)
$размер = $_POST['размер']; // Размер файла (ожидается из POST-запроса)
$время_выполнения = $_POST['время_выполнения']; // Время выполнения запроса (ожидается из POST-запроса)

// Проверка наличия данных
if (empty($путь) || empty($размер) || empty($время_выполнения)) {
    die("Путь, размер и время выполнения обязательны.");
}

// Создание подключения
$conn = new mysqli($servername, $username, $password, $dbname);

// Проверка подключения
if ($conn->connect_error) {
    die("Ошибка подключения: " . $conn->connect_error);
}

// Подготовка и выполнение INSERT-запроса
$stmt = $conn->prepare("INSERT INTO file_info (путь, размер, время_выполнения, во_сколько_запрос_сделан) VALUES (?, ?, ?, ?)");
$во_сколько_запрос_сделан = time(); // Текущее Unix-время
$stmt->bind_param("siis", $путь, $размер, $время_выполнения, $во_сколько_запрос_сделан);

if ($stmt->execute()) {
    echo "Новая запись успешно добавлена";
} else {
    echo "Ошибка: " . $stmt->error;
}

// Закрытие подключения
$stmt->close();
$conn->close();
?>
